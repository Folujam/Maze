/**
* main - entry point
* Return: int (success(0))
*/
int main(void)
{
	SDL_Instance instance;
  Uint32 last = SDL_GetTicks();

	if (init_instance(&instance) != 0)
		return (1);
  init_gameplay();
  
	/*each loop represents a frame*/
	while (SDL_TRUE)
	{
		SDL_SetRenderDrawColor(instance.renderer, 0, 0, 0, 0);
		/*clear the renderer*/
		SDL_RenderClear(instance.renderer);
		if (poll_events() == 1)
			break;
    
    Uint32 now = SDL_GetTicks();
    double dt = (now - last) / 1000.0;
    if (dt > 0.05) 
      dt = 0.05; /* clamp to avoid big jumps */
    last = now;

    update_gameplay(dt); /* Update game logic with delta time */ /* NEW: traffic light + obstacles */

    if (get_stage() == 2)
      update_ghosts(dt); /* NEW: stage 2 ghosts chasing player */

    /*render the scene*/
		render_scene(&instance);
		
    /*present the renderer*/
		SDL_RenderPresent(instance.renderer);
	}
	SDL_DestroyRenderer(instance.renderer);
	SDL_DestroyWindow(instance.window);
	TTF_Quit();
  SDL_Quit();
	return (0);
}
